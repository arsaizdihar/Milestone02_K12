import type { NextPage } from 'next';
import Head from 'next/head';
import { useRouter } from 'next/router';
import { useEffect } from 'react';
import Button from '~/components/Button';
import StudentHomePage from '~/components/StudentHomePage';
import TutorHomePage from '~/components/TutorHomePage';
import useSession from '~/hooks/useSession';
import { trpc } from '~/utils/trpc';

const Home: NextPage = () => {
  const logout = trpc.useMutation('auth.logout');
  const router = useRouter();
  const session = useSession();
  const queryClient = trpc.useContext();

  useEffect(() => {
    if (session.status === 'unauthenticated') {
      router.push('/auth/login');
    }
  }, [session]);

  return (
    <>
      <Head>
        <title>Bear With TPB</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="flex h-screen justify-center items-center">
        {!session.data ? (
          <div>Loading...</div>
        ) : (
          <div>
            <h1>{session.data.name}</h1>
            {session.data.role === 'TUTOR' ? (
              <TutorHomePage />
            ) : (
              <StudentHomePage />
            )}
            <Button
              onClick={() =>
                logout.mutate(null, {
                  onSuccess() {
                    queryClient.setQueryData(['auth.currentUser'], undefined);
                  },
                })
              }
            >
              Sign out
            </Button>
          </div>
        )}
      </div>
    </>
  );
};

export default Home;
